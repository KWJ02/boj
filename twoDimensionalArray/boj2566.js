const solution = (arr) => {
	let max = 0;
	let coorR = 0;
	let coorC = 0;
	let maxR = null;
	let maxC = null;

	for (rows of arr) {
		for (col of rows) {
			if (col > max) {
				max = col;
				maxR = coorR;
				maxC = coorC;
			}
			coorC++;
		}
		coorC = 0;
		coorR++;
	}

	console.log(max);
	console.log(maxR + 1, maxC + 1);
};

